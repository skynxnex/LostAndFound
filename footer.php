				<h3>Projektbeskrivning</h3>
				<h4>Lost and found app and site</h4>
				<p>Vi tänker oss att bygga en lost and found website vars grundkod skrivs av ad10 samt tillhörande mobilapp som skrivs av grupp 2 ad09.</p>

				<p>Siten och appen bör kunna inneha ett antal grundfunktioner som t.ex. att ladda upp ett foto på det hittade med beskrivande text. När den hittades och var. Man skall kunna surfa runt och titta på hittade grejer på ett konstruktivt sätt som även gör det kul, förutom att söka efter borttappade saker och ting som andra hittat.</p>

				<p>Appen geotaggar och visar var föremålet hittades på en karta. Foto bör alltså kunna skickas via appen på ett hittat föremål/plats, men också med tillhörande beskrivning. Man bör på ett enkelt sätt kunna utföra samma eller liknande funktionalitet på appen som på siten. Gränssnitten bör följa varandra på ett igenkännande sätt både i funktionalitet och design. Dock följer av appens begränsningar att gränssnittet pagenariseras på ett funktionellt sätt som följer av designen enligt igenkännandeprinciper.</p>
